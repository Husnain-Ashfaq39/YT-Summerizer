def remove_fillers(text):
    """Remove filler words from the input text using spaCy."""
    doc = nlp(text)
    # Filter out tokens that are in the filler words set
    filtered_text = " ".join([token.text for token in doc if token.text.lower() not in filler_words])
    # Remove extra whitespace
    return " ".join(filtered_text.split())

